a = 10
b = 3

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)
print("División entera:", a // b)
print("Módulo (resto):", a % b)
print("Exponente:", a ** b)


x = 5
y = 10

print("¿x es igual a y?", x == y)
print("¿x es menor que y?", x < y)
print("¿x diferente de y?", x != y)

# Operadores lógicos
es_mayor = x > 3
es_menor = y < 20
print("¿x > 3 y y < 20?", es_mayor and es_menor)
print("¿x > 3 o y < 5?", es_mayor or y < 5)
print("Negación:", not es_mayor)


